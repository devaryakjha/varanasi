/// Represents a Map<String, dynamic> type
typedef JSON = Map<String, dynamic>;

/// Represents a List<Map<String, dynamic>> type
typedef JSONList = List<JSON>;

/// Represents a Map<String, List<Map<String, dynamic>>> type
typedef JSONMap = Map<String, JSON>;

/// Represents a Map<String, List<Map<String, dynamic>>> type
typedef JSONListMap = Map<String, JSONList>;
